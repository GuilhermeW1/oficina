    private void atualizarTabela(){
        objProprietarioController= new ProprietarioController();
        objProprietarioController.preencher(jTable1);
    }
        
    
   
    private void limparTela(){
        txtCPFCNPJ_prop.setText("");
        txtCPF_prop.setText("");
        txtNome_prop.setText("");
    }
    
    
    private Proprietario armazenarDados(){
        try{
            Proprietario obj = new Proprietario();
            
            obj.setNome(txtNome_prop.getText().trim());
            obj.setTppessoa(txtCPFCNPJ_prop.getText().trim());
            obj.setCodigo(txtCPF_prop.getText().trim());
            
            return obj;
        }catch(Exception e){
            e.getMessage();
            return null;
        }
    }
    
    
    private boolean validarDados(){
        if(txtCPFCNPJ_prop.getText().trim().equals("")){
            CaixaDeDialogo.obterinstancia().exibirMensagem("CPF/CNPJ est√£o nulos");
            return false;
        }
        if(txtNome_prop.getText().trim().equals("")){
            CaixaDeDialogo.obterinstancia().exibirMensagem("Nome nullo");
            return false;
        }
        if(txtCPFCNPJ_prop.getText().trim().equals("")){
            CaixaDeDialogo.obterinstancia().exibirMensagem("PF/PJ nullo");
            return false;
        }
         return true;
    }
