    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user = txt_username_login.getText().trim();
        String pass = Tools.convertCharArrayToString(txt_password_login.getPassword()).trim();
        
        //this function protects the database from sql injection
        user = user.replace("'", "");
        pass = pass.replace("'", "");
        
        UserController usuario = new UserController();
        boolean existe = usuario.login(user, pass);
        System.out.println("ta funcionando porra");
        if(existe){
            TelaPrincipal tela = new TelaPrincipal();
            tela.setVisible(true);
            dispose();
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

